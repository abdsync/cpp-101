#include <iostream>
using namespace std;

int main()
{
//	ternary operator in which there is one operator and three operands 
// 	the first value of the statement must be a condition 
//  if the condition is true then the middle value is executed else the last value is sent as output
int t = 88 > 67 ? 88 : 0;
cout << "The value of t is:" << t <<endl;
}
