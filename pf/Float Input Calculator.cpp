#include <iostream>
using namespace std;
// Creating a simple calculator 
int main()  
{
	float c;
	float g;
    cout << "Please enter the value of c" << endl;
    cin  >> c;
    cout << "Please enter the value of g" << endl;
    cin  >> g;
	cout << "Addition is "       << c+g << endl;
	cout << "Subtraction is "    << c-g << endl;
	cout << "Multiplication is " << c*g << endl;
	cout << "Division is "       << c/g << endl;
	
	
	return 0;
	
	
}
